print("Exponential program")
print("------------------")
x=int(input("enter x value:"))
y=int(input("enter y value:"))
z=int(input("enter z value:"))
print("result")
p=(x*x)+(y*y)+(z*z)
print("p value:",p)
q=(x*x*x)+(y*y)+z+20
print("q value:",q)
r=(x*x)*y+(y*y)*z+(z*z)*x+10
print("r value:",r)
s=(x*x*x)*(y*y)*z+2**x*y*z+x*y*z
print("s value:",s)
      
